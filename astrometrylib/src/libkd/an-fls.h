#ifndef AN_FLS_H
#define AN_FLS_H

#include <stdint.h>
#include <assert.h>

 /*
  * flsB()  =  fls() - 1.
  *
  * Note that x MUST be > 0.
  */
static inline uint8_t an_flsB(uint32_t x) {
    assert(x > 0);
    uint8_t bit;
    for (bit = 0; x != 1; bit++)
        x = x >> 1;
    return bit;
}

#endif
